package io.omoto.fms.infrastructure.entities.mongo;

import org.joda.time.DateTime;
import org.springframework.data.annotation.CreatedDate;
import org.springframework.data.annotation.Id;
import org.springframework.data.annotation.LastModifiedDate;
import org.springframework.data.mongodb.core.index.Indexed;

import java.util.List;
import java.util.Map;
import java.util.Set;

public class ContactsDocument {
    @Id
    private String _id;

    @Indexed(unique = true)
    private String identifier;

    private Long accountId;
    private String firstName;
    private String lastName;
    private String email;
    private String phone;
    private String gender;
    private String countryCode;
    private String registrationId;
    private List<Map> qualifyingInfo;
    private Long createdBy;
    private Long updatedBy;
    private Set<String> audienceIds;

    @CreatedDate
    private DateTime createdAt;

    @LastModifiedDate
    private DateTime updatedAt;

    public String get_id() {
        return _id;
    }

    public void set_id(String _id) {
        this._id = _id;
    }

    public Long getAccountId() {
        return accountId;
    }

    public void setAccountId(Long accountId) {
        this.accountId = accountId;
    }

    public String getIdentifier() {
        return identifier;
    }

    public void setIdentifier(String identifier) {
        this.identifier = identifier;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getCountryCode() {
        return countryCode;
    }

    public void setCountryCode(String countryCode) {
        this.countryCode = countryCode;
    }

    public String getRegistrationId() {
        return registrationId;
    }

    public void setRegistrationId(String registrationId) {
        this.registrationId = registrationId;
    }

    public List<Map> getQualifyingInfo() {
        return qualifyingInfo;
    }

    public void setQualifyingInfo(List<Map> qualifyingInfo) {
        this.qualifyingInfo = qualifyingInfo;
    }

    public Long getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(Long createdBy) {
        this.createdBy = createdBy;
    }

    public Long getUpdatedBy() {
        return updatedBy;
    }

    public void setUpdatedBy(Long updatedBy) {
        this.updatedBy = updatedBy;
    }

    public DateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(DateTime createdAt) {
        this.createdAt = createdAt;
    }

    public DateTime getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(DateTime updatedAt) {
        this.updatedAt = updatedAt;
    }

    public Set<String> getAudienceIds() {
        return audienceIds;
    }

    public void setAudienceIds(Set<String> audienceIds) {
        this.audienceIds = audienceIds;
    }
}
